package generated

import (
	"testing"
)

func TestFastExtractSymbols_Go(t *testing.T) {
	src := []byte(`// Code generated by protoc-gen-go. DO NOT EDIT.
package main

type User struct {
	Name string
}

type UserServiceClient interface {
	GetUser() *User
}

func NewUser() *User {
	return &User{}
}

func (u *User) Reset() {}

var File_user_proto = "user.proto"

const Default_Name = "unknown"
`)

	summary := FastExtractSymbols("user.pb.go", src, "go")

	if len(summary.Symbols) < 5 {
		t.Fatalf("expected at least 5 symbols, got %d", len(summary.Symbols))
	}

	names := make(map[string]bool)
	for _, s := range summary.Symbols {
		names[s.Name] = true
		if s.StartLine == 0 {
			t.Errorf("symbol %s has StartLine 0", s.Name)
		}
	}

	for _, expected := range []string{"User", "UserServiceClient", "NewUser", "File_user_proto", "Default_Name"} {
		if !names[expected] {
			t.Errorf("missing symbol %q", expected)
		}
	}

	// Should have no references (fast extraction skips them)
	if len(summary.References) != 0 {
		t.Errorf("expected 0 references, got %d", len(summary.References))
	}
}

func TestFastExtractSymbols_UnknownLanguage(t *testing.T) {
	summary := FastExtractSymbols("test.xyz", []byte("hello world"), "unknown_lang")
	if len(summary.Symbols) != 0 {
		t.Errorf("expected 0 symbols for unknown language, got %d", len(summary.Symbols))
	}
}

func TestFastExtractSymbols_Python(t *testing.T) {
	src := []byte(`# Generated by the protocol buffer compiler.
class UserRequest:
    pass

class UserResponse:
    pass

def serialize():
    pass
`)

	summary := FastExtractSymbols("user_pb2.py", src, "python")
	if len(summary.Symbols) < 3 {
		t.Fatalf("expected at least 3 symbols, got %d", len(summary.Symbols))
	}
}

func TestFastExtractSymbols_GoMethodReceiver(t *testing.T) {
	src := []byte(`package main

func (s *Server) Handle() {}
func (s *Server) Close() {}
`)

	summary := FastExtractSymbols("gen.pb.go", src, "go")
	// Method receiver pattern captures the method name (last capture group)
	names := make(map[string]bool)
	for _, s := range summary.Symbols {
		names[s.Name] = true
	}
	if !names["Handle"] {
		t.Error("missing method Handle")
	}
	if !names["Close"] {
		t.Error("missing method Close")
	}
}

func TestFastExtractSymbols_LineNumbers(t *testing.T) {
	src := []byte(`package main

type Foo struct {}

func Bar() {}
`)

	summary := FastExtractSymbols("test.go", src, "go")
	for _, s := range summary.Symbols {
		if s.Name == "Foo" && s.StartLine != 3 {
			t.Errorf("Foo StartLine = %d, want 3", s.StartLine)
		}
		if s.Name == "Bar" && s.StartLine != 5 {
			t.Errorf("Bar StartLine = %d, want 5", s.StartLine)
		}
	}
}
